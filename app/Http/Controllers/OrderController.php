     *
     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse|\Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $order = Order::findOrFail($id);

        $result = $order->delete();

        if (!$result) {
            return back()->withErrors(['msg' => 'Ошибка удаления заявки']);
        }

        return redirect()
            ->route('orders.index')
            ->with(['success' => 'Заявка успешно удалена']);
